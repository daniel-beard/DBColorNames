- (NSString *)nameForColor:(UIColor *)color {
    
    CGFloat iR = 0.0f;
    CGFloat iG = 0.0f;
    CGFloat iB = 0.0f;
    CGFloat iA = 0.0f;
    
    [color getRed:&iR green:&iG blue:&iB alpha:&iA];
    
    CGFloat minDistance = CGFLOAT_MAX;
    NSString *colorKey = nil;
    
    for (NSString *key in self.colorMap) {
        @autoreleasepool {
            UIColor *currentColor = [self colorFromRGBFloat:[self.colorMap[key][kId] intValue]];
           
            CGFloat cR, cG, cB, cA = 0.0f;
            [currentColor getRed:&cR green:&cG blue:&cB alpha:&cA];
           
            // Calculate euclidean distance
            
            // Deltas
            CGFloat dR = iR - cR;
            CGFloat dG = iG - cG;
            CGFloat dB = iB - cB;
            
            CGFloat a = pow(dR, 2.0f);
            CGFloat b = pow(dG, 2.0f);
            CGFloat c = pow(dB, 2.0f);
            
            CGFloat d = sqrt(a + b + c);
            
            if (d < minDistance) {
                minDistance = d;
                colorKey = key;
            }
        }
    }
    return colorKey;
}


