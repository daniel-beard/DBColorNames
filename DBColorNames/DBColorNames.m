- (UIColor *)colorFromRGBFloat:(int)rgbValue {
    return [UIColor
            colorWithRed : ((float)((rgbValue & 0xFF0000) >> 16)) / 255.0
            green : ((float)((rgbValue & 0xFF00) >> 8)) / 255.0
